    <script>
    $(document).ready(function() {
        var base_url = '<?= base_url() ?>';
        getAllData();
        function getAllData(){
            $.ajax({
                url:  '<?= site_url() ?>shops/getUkmDataDetailProduk',
                type: 'GET',
                data: {
                    productName: '<?= $this->input->get('productName') ?>'
                },
                async: true,
                cache: false,
                dataType: 'json',
                beforeSend: function() {
                    $('#h3_name').empty();
                    $('#h2_price').empty();
                    $('#font_category').empty();
                    $('#font_stok').empty();
                    $('#p_description').empty();
                },
                complete: function() {
                },
                success: function(response) {
                    if (response.data.product[0]['is_diskon'] == 1) {
						var price = '<strike>Rp. ' + response.data.product[0]['price'] + '</strike> <br/>' +
                        'Rp. ' + response.data.product[0]['final_price'];
                        $('#h2_price').append(price);
					}else{
                        var price = 'Rp. ' + response.data.product[0]['price'];
                        $('#h2_price').text(price);
                    }

                    if (response.data.product[0]['stock'] == 0) {
						var stock = 'Habis';
					}else{
                        var stock = 'Tersedia '+ response.data.product[0]['stock'];
                    }
                    
                    $('#h3_name').text(response.data.product[0]['name']);
                    $('#font_category').text(response.data.product[0]['category_name']);
                    $('#font_stock').text(stock);
                    $('#p_description').text(response.data.product[0]['description']);
                }
            });
        }

        $("#commentForm").submit(function(event) {
				$.ajax({
					url: '<?= site_url() ?>ukms/processAddComent/',
					data: $(this).serialize(),
					type: 'POST',
                    dataType: 'json',
                    async: true,
                    cache: false,
                    dataType: 'json',
                    beforeSend: function() {
                    },
                    complete: function() {
                    },
					success: function(response) {
                        getAllData();
					}
				});
				event.preventDefault();
			});
	});
	
	function processAdd(productID){
		var qty = $("#sst").val();
		$.ajax({
			url:  '<?= site_url() ?>carts/proccessAdd',
			data: {
				productID: productID,
				qty: qty
			},
			type: 'POST',
			async: true,
			cache: false,
			dataType: 'json',
			beforeSend: function() {
				$("#totalCart").empty();
			},
			complete: function() {
			},
			success: function(response) {
				$("#totalCart").text(response.data.totalCart);
			}
		});
	}
    </script>